    public void setMos1(double mos1) {
        this.mos1 = mos1;
    }

    public double getMos2() {
        return mos2;
    }

    public void setMos2(double mos2) {
        this.mos2 = mos2;
    }

    public double getMos3() {
        return mos3;
    }

    public void setMos3(double mos3) {
        this.mos3 = mos3;
    }

    public double getMos4() {
        return mos4;
    }

    public void setMos4(double mos4) {
        this.mos4 = mos4;
    }

    public double getMos5() {
        return mos5;
    }

